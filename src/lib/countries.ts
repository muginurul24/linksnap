const countryNameFormatter =
  typeof Intl.DisplayNames === "function"
    ? new Intl.DisplayNames(["en"], { type: "region" })
    : null;

function getCountryName(code: string): string {
  return countryNameFormatter?.of(code) ?? code;
}

export function getCountryFlagEmoji(code: string): string {
  const normalizedCode = code.trim().toUpperCase();
  if (!/^[A-Z]{2}$/.test(normalizedCode)) return "";

  return Array.from(normalizedCode)
    .map((letter) => String.fromCodePoint(127397 + letter.charCodeAt(0)))
    .join("");
}

function normalizeSearchText(value: string): string {
  return value
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .trim()
    .toLowerCase();
}

export const COUNTRIES: Country[] = COUNTRY_CODES.map((code) => ({
  code,
  flag: getCountryFlagEmoji(code),
  name: getCountryName(code),
})).sort((first, second) =>
  first.name.localeCompare(second.name, "en", { sensitivity: "base" }),
);

export function filterCountries(
  query: string,
  countries: readonly Country[] = COUNTRIES,
): Country[] {
  const normalizedQuery = normalizeSearchText(query);
  if (!normalizedQuery) return [...countries];

  return countries.filter((country) => {
    const name = normalizeSearchText(country.name);
    const code = normalizeSearchText(country.code);
    return name.includes(normalizedQuery) || code.includes(normalizedQuery);
  });
}

export function getCountryByCode(
  code: string | null | undefined,
  countries: readonly Country[] = COUNTRIES,
): Country | null {
  if (!code) return null;

  const normalizedCode = code.trim().toUpperCase();
  return countries.find((country) => country.code === normalizedCode) ?? null;
}

export function getNextCountryIndex({
  currentIndex,
  direction,
  total,
}: {
  currentIndex: number;
  direction: "next" | "previous";
  total: number;
}): number {
  if (total <= 0) return -1;
  if (currentIndex < 0) return direction === "next" ? 0 : total - 1;

  return direction === "next"
    ? (currentIndex + 1) % total
    : (currentIndex - 1 + total) % total;
}
